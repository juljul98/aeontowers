function definity_vc_iconpicker_base_register_css() {
	// Linea Font CSS
	wp_enqueue_style( 'linea_font', DEFINITY_URI . '/assets/fonts/linea-font/css/linea-font.css' );
	// ET Line Icons CSS
	wp_enqueue_style( 'etlineicon_font', DEFINITY_URI . '/assets/fonts/et-lineicons/css/style.css' );
	// Crypto Icons CSS
	wp_enqueue_style( 'cryptocoins', DEFINITY_URI . '/assets/fonts/cryptocoins/cryptocoins.css' );
};
add_action( 'vc_base_register_admin_css', 'definity_vc_iconpicker_base_register_css' );


